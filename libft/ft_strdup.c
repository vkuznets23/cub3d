#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		lenght;
	char	*dest;

	lenght = 0;
	i = 0;
	while (s1[lenght])
		lenght++;
	dest = (char *)malloc(sizeof(char) * (lenght + 1));
	if (!dest)
		return (0);
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
